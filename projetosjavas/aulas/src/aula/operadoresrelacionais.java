package aula;

public class operadoresrelacionais {
    public static void main(String[] args) {
    /*
    == igual
    < menor
    > maior
    <= menor igual
    >= maior igual
    != diferente
     */

     int valor1 = 20;
     int valor2 = 10;

     System.out.println(valor1 ==valor2);
     System.out.println(valor1 < valor2);
     System.out.println(valor1 > valor2);
     System.out.println(valor1 <= valor2);
     System.out.println(valor1 >= valor2);
     System.out.println(valor1 != valor2);
    }
}
